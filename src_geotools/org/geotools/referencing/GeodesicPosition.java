package org.geotools.referencing;

public class GeodesicPosition {
	double X;
	double Y;
	public GeodesicPosition(double x, double y) {
		super();
		X = x;
		Y = y;
	}
	public double getX() {
		return X;
	}
	public void setX(double x) {
		X = x;
	}
	public double getY() {
		return Y;
	}
	public void setY(double y) {
		Y = y;
	}

	
}
